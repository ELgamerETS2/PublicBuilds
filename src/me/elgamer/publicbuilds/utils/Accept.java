package me.elgamer.publicbuilds.utils;

public class Accept {
	
	int size = 1;
	int accuracy = 1;
	int quality = 1;

	public void setSize(int x) {
		size = x;
	}
	
	public void setAccuracy(int x) {
		accuracy = x;
	}
	
	public void setQuality(int x) {
		quality = x;
	}	
	
	public int size() {
		return size;
	}
	
	public int accuracy() {
		return accuracy;
	}
	
	public int quality() {
		return quality;
	}
	
	
}
